// Detect provider
async function getProvider() {
  const provider = await detectEthereumProvider();

  if (provider) {
    console.log('Ethereum provider detected:', provider);
    return new ethers.providers.Web3Provider(provider);
  } else {
    console.error('Please install MetaMask or another Ethereum-compatible wallet.');
    throw new Error('Ethereum provider not found');
  }
}

// Get signer
async function getSigner() {
  const provider = await getProvider();
  return provider.getSigner();
}

// Get YURI+ contract instance (Read-only)
export async function getYuriPlusContractInstance() {
  const provider = await getProvider();
  return new ethers.Contract(yuriPlusContractAddress, yuriPlusAbi, provider);
}

// Get YURI+ contract instance with signer (For transactions)
export async function getYuriPlusContractWithSigner() {
  const signer = await getSigner();
  return new ethers.Contract(yuriPlusContractAddress, yuriPlusAbi, signer);
}

// Example function: Check YURI+ balance
export async function getYuriPlusBalance(account: string) {
  const contract = await getYuriPlusContractInstance();
  try {
    const balance = await contract.balanceOf(account);
    return ethers.utils.formatUnits(balance, 18); // Assuming YURI+ has 18 decimals
  } catch (err) {
    console.error("Error getting YURI+ balance:", err);
    return null;
  }
}

// Example function: Transfer YURI+ tokens
export async function transferYuriPlusTokens(toAddress: string, amount: string) {
  const contract = await getYuriPlusContractWithSigner();
  try {
    const tx = await contract.transfer(toAddress, ethers.utils.parseUnits(amount, 18));
    await tx.wait();
    return tx;
  } catch (err) {
    console.error("Error transferring YURI+ tokens:", err);
    return null;
  }
}

// Example function: Approve spending of YURI+ tokens
export async function approveYuriPlusSpender(spenderAddress: string, amount: string) {
  const contract = await getYuriPlusContractWithSigner();
  try {
    const tx = await contract.approve(spenderAddress, ethers.utils.parseUnits(amount, 18));
    await tx.wait();
    return tx;
  } catch (err) {
    console.error("Error approving YURI+ spender:", err);
    return null;
  }
}
